package com.github.a2g.core.authoredscene;


public class Point {
    private int x;
    private int y;

    public Point(int x, int y) {
        this.setX(x);
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

}
